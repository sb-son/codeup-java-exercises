public class StringExercise {
    public static void main(String[] args) {
        String str = "We don't need no education\nWe don't need no thought control";
        System.out.println(str);

        String quotations = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotations);

        String mainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(mainDrive);

        String backslash = "I can do backslashes \\, double backslashes \\\\,\n and the amazing triple backslash\\\\\\";
        System.out.println(backslash);
    }
}
